makeDoubleQuoted = (text) =>
    text.replaceAll(/^'|'$|((?<=([., ]))['](?=([,.]|\w|\s)))/g, '"')

console.log(makeDoubleQuoted("'I'm the cook,' he said, 'it's my job.'"))

